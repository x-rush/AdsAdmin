package com.erpdemo.vo;

public class NewsVO {

	private String year;
	
	private int count;

	public NewsVO() {
		
	}
	
	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	
	
}
